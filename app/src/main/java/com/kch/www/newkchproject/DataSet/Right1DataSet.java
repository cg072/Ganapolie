public class Right1DataSet {

    String date;
    String msg;

    public Right1DataSet(String date, String msg) {
        this.date = date;
        this.msg = msg;
    }

    public String getDate() {
        return date;
    }

    public void setData(String date) {
        this.date = date;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
